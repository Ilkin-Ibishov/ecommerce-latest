async function seed() {
  console.log("🚀 Seeding ILK Electronics catalog...\n");

  const catIdMap = {};

  console.log("📂 Creating categories...");
  for (const cat of CATEGORIES) {
    const { data: existing } = await admin.from("categories").select("id").eq("slug", cat.slug).maybeSingle();
    if (existing) {
      catIdMap[cat.slug] = existing.id;
      console.log(`  ⏭  Category "${cat.slug}" already exists`);
      continue;
    }

    const { data, error } = await admin.from("categories").insert({
      slug: cat.slug,
      icon_url: cat.icon,
    }).select("id").single();

    if (error) { console.error(`  ❌ Error creating ${cat.slug}:`, error.message); continue; }

    catIdMap[cat.slug] = data.id;

    const translations = [
      { category_id: data.id, locale: "az", name: cat.az, description: cat.az_desc },
      { category_id: data.id, locale: "ru", name: cat.ru, description: cat.ru_desc },
      { category_id: data.id, locale: "en", name: cat.en, description: cat.az_desc },
    ];
    await admin.from("category_translations").insert(translations);
    console.log(`  ✅ Created category: ${cat.slug}`);
  }

  console.log("\n📦 Creating products...");
  let totalCreated = 0;
  let totalSkipped = 0;

  for (const [catSlug, products] of Object.entries(PRODUCTS)) {
    const catId = catIdMap[catSlug];
    if (!catId) { console.warn(`  ⚠️  No category ID for ${catSlug}`); continue; }

    console.log(`\n  📁 Category: ${catSlug} (${products.length} products)`);

    for (const p of products) {
      const { data: existing } = await admin.from("products").select("id").eq("slug", p.slug).maybeSingle();
      if (existing) {
        console.log(`    ⏭  "${p.slug}" exists`);
        totalSkipped++;
        continue;
      }

      const { data: prod, error: prodErr } = await admin.from("products").insert({
        sku: p.sku,
        slug: p.slug,
        price: p.price,
        original_price: p.original_price,
        brand: p.brand,
        stock: Math.floor(Math.random() * 40) + 5,
        is_featured: Math.random() > 0.75,
        is_on_sale: p.original_price > p.price,
        sort_order: 0,
      }).select("id").single();

      if (prodErr) { console.error(`    ❌ Error: ${p.slug}:`, prodErr.message); continue; }

      const translations = [
        { product_id: prod.id, locale: "az", name: p.az.name, description: p.az.desc },
        { product_id: prod.id, locale: "ru", name: p.ru.name, description: p.ru.desc },
        { product_id: prod.id, locale: "en", name: p.az.name, description: p.az.desc },
      ];
      await admin.from("product_translations").insert(translations);

      await admin.from("product_images").insert({
        product_id: prod.id,
        url: img(p.img),
        alt_text: p.az.name,
        sort_order: 0,
      });

      await admin.from("product_categories").insert({
        product_id: prod.id,
        category_id: catId,
      });

      if (p.specs?.length) {
        await admin.from("product_specs").insert(
          p.specs.map(([k, v], i) => ({
            product_id: prod.id,
            spec_key: k,
            spec_value: v,
            sort_order: i,
          }))
        );
      }

      console.log(`    ✅ ${p.az.name} (${p.price} AZN)`);
      totalCreated++;
    }
  }

  console.log(`\n✨ Done! Created ${totalCreated} products, skipped ${totalSkipped} existing.`);
  console.log(`📂 ${CATEGORIES.length} categories ready.`);
}

seed().catch((e) => { console.error(e); process.exit(1); });
